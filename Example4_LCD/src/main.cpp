void setup()
{

  Serial.begin(9600);
  delay(1000);
  Wire.begin(2, 14); //SDA=13,SCL=12

  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C))
  {
    Serial.println("SSD1306 allocation failed");
  }
  else
  {
    Serial.println("SSD1306 init OK!");
  }

  display.setTextColor(WHITE);
  display.clearDisplay();
  display.drawBitmap(0, 0, logo1, 128, 64, 1);
  display.display();
  delay(3000);  
}

void loop()
{

  // clear display
  display.clearDisplay();


  display.setTextSize(1);   //字體大小
  display.setCursor(0, 7);  //起始位置(寬,高)
  display.print("Hello World!");

  display.setTextSize(2);
  display.setCursor(10, 25);
  display.print("Count: ");
  display.print(count);

  display.setCursor(10, 50);
  //Enable ASCII Code
  display.cp437(true);   
  // Ref: https://theasciicode.com.ar/  
  display.write(26);
  display.write(0);
  display.write(2);
  display.write(0);
  display.write(3);
  display.write(0);
  display.write(14);
  display.write(0);
  display.write(15);

  display.display();

  count++;

  delay(1000);
  // 其他動作可參考 https://lastminuteengineers.com/oled-display-esp8266-tutorial/
}